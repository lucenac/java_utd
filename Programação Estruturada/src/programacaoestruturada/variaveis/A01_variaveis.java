public class A01_variaveis { //pascalcase
    public static void main(String[] args) {
        // Declaração e inicialização de variáveis
        int idade = 25;         // Variável do tipo inteiro
        double altura = 1.75;   // Variável do tipo double (número decimal)
        char letra = 'A';       // Variável do tipo caractere
        boolean estaChovendo = false;  // Variável booleana (verdadeiro ou falso)
        String nome = "Maria";  // Variável do tipo String (sequência de caracteres)

        // Outros tipos de variáveis
        byte idadeByte = 30;    // Byte (números de -128 a 127)
        short temperatura = -10; // Short (números de -32768 a 32767)
        long populacao = 1000000000L; // Long (números inteiros longos)
        float peso = 65.5f;     // Float (número decimal menor que double)
    }
}
